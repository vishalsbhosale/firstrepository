package com.method;

public class All {

	public static void main(String[] args) {
		int a=10;
		int b=20;
		//Arithmatic operator
		System.out.println(a+b);
		System.out.println(a-b);
		System.out.println(a*b);
		System.out.println(a/b);
		System.out.println(a%b);
		//Relational operator
		System.out.println(a==b);
		System.out.println(a<b);
		System.out.println(a>b);
		System.out.println(a<=b);
		System.out.println(a>=b);
		System.out.println(a!=b);
		//increment / decrement operator
		a++;
		b--;
		
				System.out.println(a);
		System.out.println(b);
		//Logical operator
		boolean x=true;
		boolean y=false;
		System.out.println(x&&y);
		System.out.println(x||y);
		System.out.println(!x);
		
	}
}
